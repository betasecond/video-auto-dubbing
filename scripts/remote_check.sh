#!/bin/bash
# 远程服务器检查脚本
# 请在 SSH 登录后执行此脚本

echo "========================================="
echo "1. 检查运行的 API 服务进程"
echo "========================================="
ps aux | grep -E "python.*api_server|uvicorn" | grep -v grep
echo ""

echo "========================================="
echo "2. 查看可用的参考音频文件"
echo "========================================="
find /root/index-tts-vllm -type f \( -name "*.wav" -o -name "*.mp3" \) 2>/dev/null | head -20
echo ""

echo "========================================="
echo "3. api_server_v2.py 文件内容"
echo "========================================="
cat /root/index-tts-vllm/api_server_v2.py
echo ""

echo "========================================="
echo "4. 检查服务器监听的端口"
echo "========================================="
netstat -tlnp | grep -E ":8000|:8080|:7860" || ss -tlnp | grep -E ":8000|:8080|:7860"
echo ""

echo "========================================="
echo "完成！请将以上输出复制回来"
echo "========================================="
